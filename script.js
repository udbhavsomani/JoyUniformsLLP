const drawer = document.getElementById('solution-drawer');
const drawerContent = document.getElementById('drawer-content');
const requirementSelect = document.getElementById('requirement');
const menuToggle = document.querySelector('.menu-toggle');
const nav = document.getElementById('primary-nav');
let lastFocusedElement = null;

const solutionOrder = ['school','healthcare','industrial','corporate','hospitality','security','fuel','custom'];

function renderSolutionShowcases() {
  const showcaseList = document.getElementById('solution-showcase-list');
  if (!showcaseList) return;
  showcaseList.innerHTML = solutionOrder.map((key, index) => {
    const item = solutionData[key];
    return `
      <article class="showcase-card reveal" id="sector-${key}">
        <a class="showcase-card-media" href="${item.url}" aria-label="Open ${item.kicker} page">
          <img src="${item.image}" alt="${item.imageAlt}" width="1448" height="1086" loading="lazy">
        </a>
        <div class="showcase-card-copy">
          <span class="showcase-code">${String(index + 1).padStart(2, '0')} • ${item.kicker}</span>
          <h3><a href="${item.url}">${item.title}</a></h3>
          <p>${item.lead}</p>
          <div class="showcase-tags">${item.tags.map(tag => `<span>${tag}</span>`).join('')}</div>
          <div class="showcase-actions compact-actions">
            <a class="button button-small" href="${item.url}">Open category page</a>
            <button class="button button-ghost-dark button-small" type="button" data-open-solution="${key}">Quick view</button>
          </div>
        </div>
      </article>`;
  }).join('');
}

renderSolutionShowcases();

function openSolution(key, updateHash = true) {
  const item = solutionData[key];
  if (!item) return;
  lastFocusedElement = document.activeElement;
  drawerContent.innerHTML = `
    <span class="drawer-kicker">${item.kicker}</span>
    <div class="drawer-hero"><img src="${item.image}" alt="${item.imageAlt}" width="1448" height="1086"></div>
    <h2 id="drawer-title">${item.title}</h2>
    <p class="drawer-lead">${item.lead}</p>
    <div class="drawer-band">${item.tags.map(tag => `<span>${tag}</span>`).join('')}</div>
    <div class="drawer-section">
      <h3>Typical garments and solutions</h3>
      <ul class="drawer-list">${item.includes.map(line => `<li>${line}</li>`).join('')}</ul>
    </div>
    <div class="drawer-section">
      <h3>Our approach</h3>
      <p>${item.approach}</p>
    </div>
    <div class="drawer-callout">${item.note}</div>
    <div class="drawer-actions">
      <a class="button" href="#contact" data-drawer-quote="${item.select}">Discuss This Requirement</a>
      <button class="button button-ghost-dark" type="button" data-close-drawer>View All Solutions</button>
    </div>`;
  drawer.classList.add('open');
  drawer.setAttribute('aria-hidden', 'false');
  document.body.classList.add('drawer-open');
  drawer.querySelector('.drawer-close').focus();
  if (updateHash) history.replaceState(null, '', `#solution-${key}`);
}

function closeDrawer(updateHash = true) {
  drawer.classList.remove('open');
  drawer.setAttribute('aria-hidden', 'true');
  document.body.classList.remove('drawer-open');
  if (updateHash && location.hash.startsWith('#solution-')) history.replaceState(null, '', '#solutions');
  if (lastFocusedElement) lastFocusedElement.focus();
}

document.addEventListener('click', (event) => {
  const opener = event.target.closest('[data-open-solution]');
  if (opener) {
    event.preventDefault();
    openSolution(opener.dataset.openSolution);
    nav.classList.remove('open');
    menuToggle.setAttribute('aria-expanded', 'false');
  }
  if (event.target.closest('[data-close-drawer]')) closeDrawer();
  const drawerQuote = event.target.closest('[data-drawer-quote]');
  if (drawerQuote) {
    const value = drawerQuote.dataset.drawerQuote;
    setRequirement(value);
    closeDrawer(false);
    history.replaceState(null, '', '#contact');
    setTimeout(() => document.getElementById('contact').scrollIntoView(), 80);
  }
  const prefill = event.target.closest('[data-prefill]');
  if (prefill) setRequirement(prefill.dataset.prefill);
});

function setRequirement(value) {
  if (!requirementSelect) return;
  const options = [...requirementSelect.options];
  const match = options.find(opt => opt.textContent.trim() === value || opt.value === value);
  if (match) requirementSelect.value = match.value;
}

menuToggle.addEventListener('click', () => {
  const open = !nav.classList.contains('open');
  nav.classList.toggle('open', open);
  menuToggle.setAttribute('aria-expanded', String(open));
});

document.querySelectorAll('.primary-nav a').forEach(link => link.addEventListener('click', () => {
  nav.classList.remove('open');
  menuToggle.setAttribute('aria-expanded', 'false');
}));

document.addEventListener('keydown', (event) => {
  if (event.key === 'Escape' && drawer.classList.contains('open')) closeDrawer();
});

function handleHash() {
  if (location.hash.startsWith('#solution-')) {
    const key = location.hash.replace('#solution-', '');
    if (solutionData[key]) openSolution(key, false);
  }
}
window.addEventListener('hashchange', handleHash);
handleHash();

const revealObserver = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.style.setProperty('--delay', `${entry.target.dataset.delay || 0}ms`);
      entry.target.classList.add('visible');
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: .12 });
document.querySelectorAll('.reveal').forEach(el => revealObserver.observe(el));

const header = document.getElementById('site-header');
window.addEventListener('scroll', () => header.classList.toggle('scrolled', window.scrollY > 20), { passive: true });

document.getElementById('year').textContent = new Date().getFullYear();

const form = document.getElementById('enquiry-form');
const formStatus = document.getElementById('form-status');
form.addEventListener('submit', async (event) => {
  event.preventDefault();
  formStatus.className = 'form-status';
  formStatus.textContent = '';
  const requiredFields = [...form.querySelectorAll('[required]')];
  requiredFields.forEach(field => field.classList.remove('invalid'));
  const invalid = requiredFields.filter(field => !field.checkValidity());
  if (invalid.length) {
    invalid.forEach(field => field.classList.add('invalid'));
    invalid[0].focus();
    formStatus.classList.add('error');
    formStatus.textContent = 'Please complete the required fields before submitting.';
    return;
  }
  const button = form.querySelector('button[type="submit"]');
  const original = button.innerHTML;
  button.disabled = true;
  button.innerHTML = '<span>Sending…</span>';
  try {
    const response = await fetch('https://formsubmit.co/ajax/info@joyuniforms.co.in', {
      method: 'POST',
      headers: { 'Accept': 'application/json' },
      body: new FormData(form)
    });
    if (!response.ok) throw new Error('Submission failed');
    form.reset();
    formStatus.classList.add('success');
    formStatus.textContent = 'Thank you. Your enquiry has been sent to Joy Uniforms.';
  } catch (error) {
    formStatus.classList.add('error');
    formStatus.innerHTML = 'The form service could not be reached. Please email <a href="mailto:info@joyuniforms.co.in">info@joyuniforms.co.in</a> or use WhatsApp.';
  } finally {
    button.disabled = false;
    button.innerHTML = original;
  }
});
